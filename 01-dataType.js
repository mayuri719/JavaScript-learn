//Primitive Datatype : Number, String and Boolean
console.log("================Number datatype=================");

var age=22;
console.log("Age is",age);
console.log("Datatype of Age is : ",typeof age);

console.log("================String datatype=================");
var city="pune";
console.log("City Name is :",city);
console.log("DataType of City is :",typeof city);

var marks="89";
console.log("Marks are :", marks);
console.log("Datatype Of Marks is :", typeof marks);

console.log("================Boolean datatype=================");
var IsVisible=true;
console.log(IsVisible);
console.log("Datatype of IsVisible is :", typeof IsVisible);

//=================Special Datatypes : undefined , null==============

console.log("================Special datatype=================");
var country;//it means we declare variable but not assign any value.
console.log("Country is :",country);
console.log("Datatype of Country is :", typeof country);

var village = null;
console.log("Country is :",village);
console.log("Datatype of Village is :", typeof village);